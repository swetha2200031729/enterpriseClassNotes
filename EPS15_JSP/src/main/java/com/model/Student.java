package com.model;

public class Student {
	int sid;
	String sname;
	int cgp;
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public int getCgp() {
		return cgp;
	}
	public void setCgp(int cgp) {
		this.cgp = cgp;
	}
	

}
